package algcomp.alg;
//solution class for cuckoo search.
public class Egg {
	
	double x;
	double y;
	double eval;
	
	public Egg(double _x, double _y){
		x = _x;
		y = _y;
		
	}
	public double getEval() {
		return eval;
	}
	public void setEval(double eval) {
		this.eval = eval;
	}
	public double getX() {
		return x;
	}
	public double getY() {
		return y;
	}
	
	
	

}
